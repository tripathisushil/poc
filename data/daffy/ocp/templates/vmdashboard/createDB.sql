
DROP DATABASE IF EXISTS @VM_DASHBOARD_DB_NAME@;
DROP USER IF EXISTS '@VM_DASHBOARD_USER@'@'localhost';
CREATE DATABASE @VM_DASHBOARD_DB_NAME@;
CREATE USER '@VM_DASHBOARD_USER@'@'localhost' IDENTIFIED BY '@VM_DASHBOARD_PASSWORD@';
GRANT ALL PRIVILEGES ON @VM_DASHBOARD_DB_NAME@.* to '@VM_DASHBOARD_USER@'@'localhost';
FLUSH PRIVILEGES;
